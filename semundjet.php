AIDS (sida)<br>

AIDS rrjedh nga gjuha angleze Acquired Imunodefficiency Sindrom apo SIDA qe rrjedh nga gjuha frenge, Syndrome d’Immuno Déficience Acquise. Nga te dy emrat kuptohet se AIDS eshte sindromi i fituar nga virusi i cili demton sistemin e imunitetit. AIDS eshte nje koleksion i infektimeve dhe kacereve te rralla qe njerezve me HIV pozitiv mund t’iu zhvillohen. Nese nje person me HIV semuret me njeren nga keto semundje te veçanta, atehere do te thote se vuan nga AIDS. Shume nga keto organizma qe i shkaktojne keto semundje jane mjaft te zakonshme dhe relativisht te pademshme per personat me imunitet te shendoshe. Megjithate, te personat me imunitet te demtuar keqesisht, mund te shkaktojne semundje serioze dhe vdekje. Kater menyrat kryesore per t’u infektuar me HIV jane: Duke pasur marredhenie seksuale vaginale dhe anale pa perdorim te kondomit me dike qe eshte i/e infektuar me HIV; duke marre droge me pajisje te injeksioneve qe eshte e kontaminuar me gjak te infektuar; foshnjat gjate shtatzenise nga nenat e infektuara me HIV, gjate lindjes apo ushqimit me gji; me injeksion apo transfuzion gjaku nga personi i infektuar. HIV nuk ngjitet nga kontaktet sociale ditore, si: Puthjet ne faqe, prekjet, perqafimet, takimi me duar, larja ne pishine, etj.<br><br>
ALZHEIMER (ALZHEIMER)<br>

Me kalimin e moshes shfaqen simptoma te ndryshme, te cilat tregojne se nje person ka probleme me kujtesen. Keto simptoma nuk jane te qarta, sepse mund te duken krejt normale ne disa situata ose duke u bazuar ne faktin se ka te beje mosha. Rreth 85 per qind e gjashtedhjetevjeçareve ne te gjithe boten vuan nga humbja e kujteses. Kjo perben nje nivel epidemiologjik shume te larte. Ka raste kur semundja e kujteses, e njohur si sindroma Alzheimer, lind edhe para moshes 65-vjeçare, por keto raste jane te rralla dhe karakterizohen nga histori te veçanta. Sistemet shendetesore te vendeve perendimore kane humbje shume te medha prej kesaj semundjeje. Nderkohe qe, sipas specialisteve, rreth moshes 40-vjeçare nisin te shfaqen simptomat e para te humbjes se kujteses. Simptomat vijne si rezultat i demtimit te qelizave nervore, te cilat ngadalesojne percjelljen e sinjalit per ne tru dhe anasjelltas. Per kete shkak keta persona harrojne dhe me kalimin e kohes kjo simptome behet edhe me e theksuar. Nje simptome tjeter eshte anomia, mosgjetja e emrave dhe fjaleve gjate diskutimeve. Femrat jane me te prekura nga kjo sindrome dhe kohet e fundit vihet re nje rritje e numrit te te prekurve nga kjo semundje.<br><br>
ASTHMA (ASTMA)<br>

Astma bronkiale, nje semundje kronike serioze, qe dita-dites po shpeshtohet dhe futet nder problemet shendetesore boterore. Ne Shtetet e Bashkuara te Amerikes, prej 1965-1983, sulmet astmatike u katerfishuan, ne Angli u trefishuan; nga kombinimi i faktoreve mjekesore, shoqerore, psikike dhe te faktoreve te ambientit jetesor. Shkaktaret e astmes, si edhe te semundjeve tjera atopike, jane alergjenet e ambientit jetesor dhe te disa llojeve te ushqimit. Astmen e shkaktojne poleni apo pluhuri i shtepise. Rolin kryesor ne shfaqjen e astmes e luan trashegimia. Trashegohet paraprirja per te reaguar me manifestime alergjike ne kontakt me alergjenet e caktuara; ne rastin e astmes me reagime pezmatuese alergjike te mukozave bronkiale, qe manifestohet me sulme astmatike. Pra, per t’u shfaqur astma, nevojitet prirja e trasheguar dhe kontakti me lenden alergjike.<br><br></p>




